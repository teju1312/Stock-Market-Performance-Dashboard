  index_category VARCHAR(50) NOT NULL,
  `index` VARCHAR(50) NOT NULL,
  symbol VARCHAR(100) NOT NULL,
  open DECIMAL(12, 2) NOT NULL,
  high DECIMAL(12, 2) NOT NULL,
  low DECIMAL(12, 2) NOT NULL,
  prev_close DECIMAL(12, 2) NOT NULL,
  ltp DECIMAL(12, 2),
